    private void listCars() {

        cbxCars.removeAllItems(); //usamos este metodo para que no salga la opcion seleccione varias veces
        Car car1 = new Car(); // hacemos una instancia de Car para mostrar un carro ("--SELECCIONE--")
        car1.setMarca(SELECCIONE); // lo colocamos en marca por el metodo to string de Car 
        car1.setModelo(""); // concatenamos en el conbo box lo que queramos que nos muestre de carro
        cbxCars.addItem(car1); //agregamos el nuevo carro (--SELECCIONE--) al combo box
        tblCars.removeAll(); //limpiamos la tabla para que no se muestre la opciones repetidas

        DefaultTableModel defaultTableModel = new DefaultTableModel();
        for (String COLUMN : COLUMNS) {
            defaultTableModel.addColumn(COLUMN);
        }
        tblCars.setModel(defaultTableModel);

        try {
            List<Car> cars = carController.obtenerCarros();
            if (cars.isEmpty()) {
                return;
            }
            defaultTableModel.setRowCount(cars.size());
            int row = 0;
            for (Car car : cars) {
                defaultTableModel.setValueAt(car.getId(), row, 0);
                defaultTableModel.setValueAt(car.getMarca(), row, 1);
                defaultTableModel.setValueAt(car.getModelo(), row, 2);
                defaultTableModel.setValueAt(car.getAnho(), row, 3);
                defaultTableModel.setValueAt(car.getColor(), row, 4);
                defaultTableModel.setValueAt(car.getTransmission(), row, 5);
                row++;

                cbxCars.addItem(car);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }

    }

    private void addListener() {
        cbxCars.addItemListener(event -> {
            Car selectedCar = (Car) event.getItem();
            if (selectedCar.getMarca().equals(SELECCIONE)) {
                txtCarId.setText("");
                txtBrandEdit.setText("");
                txtModelEdit.setText("");
                txtAnhoEdit.setText("");
                txtColorEdit.setText("");
                txtTransmissionEdit.setText("");

            } else {
                txtCarId.setText(String.valueOf(selectedCar.getId()));
                txtBrandEdit.setText(selectedCar.getMarca());
                txtModelEdit.setText(selectedCar.getModelo());
                txtAnhoEdit.setText(selectedCar.getAnho());
                txtColorEdit.setText(selectedCar.getColor());
                txtTransmissionEdit.setText(selectedCar.getTransmission());

            }
            System.out.println("selectedCard = " + selectedCar);

        });
    }

