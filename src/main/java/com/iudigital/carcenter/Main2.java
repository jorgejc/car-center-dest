    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        if (txtBrand.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite marca");
            txtBrand.requestFocus();
            return;
        }

        if (txtModel.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debe digitar Modelo");
            txtBrand.requestFocus();
            return;
        }

        if (txtAnho.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Año");
            txtBrand.requestFocus();
            return;
        }

        if (txtColor.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Color");
            txtBrand.requestFocus();
            return;
        }

        if (txtTransmission.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite tipo Transmisión");
            txtBrand.requestFocus();
            return;
        }

        Car car = new Car();
        car.setMarca(txtBrand.getText().trim());
        car.setModelo(txtModel.getText().trim());
        car.setAnho(txtAnho.getText().trim());
        car.setColor(txtColor.getText().trim());
        car.setTransmission(txtTransmission.getText().trim());

        try {
            carController.crear(car);
            txtBrand.setText("");
            txtModel.setText("");
            txtAnho.setText("");
            txtColor.setText("");
            txtTransmission.setText("");
            listCars();
            JOptionPane.showMessageDialog(null, "Carro creado con éxito");
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Carro no pudo ser creado");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

