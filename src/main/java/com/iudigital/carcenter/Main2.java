    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if (txtCarId.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccionar un carro de la Lista");
            txtCarId.requestFocus();
            return;
        }

        if (txtBrandEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite marca");
            txtBrandEdit.requestFocus();
            return;
        }

        if (txtModelEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Debe digitar Modelo");
            txtModelEdit.requestFocus();
            return;
        }

        if (txtAnhoEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Año");
            txtAnhoEdit.requestFocus();
            return;
        }

        if (txtColorEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite Color");
            txtColorEdit.requestFocus();
            return;
        }

        if (txtTransmissionEdit.getText().trim().length() == 0) {
            JOptionPane.showMessageDialog(null, "Digite tipo Transmisión");
            txtTransmissionEdit.requestFocus();
            return;
        }

        Car car = new Car();
        car.setMarca(txtBrandEdit.getText().trim());
        car.setModelo(txtModelEdit.getText().trim());
        car.setAnho(txtAnhoEdit.getText().trim());
        car.setColor(txtColorEdit.getText().trim());
        car.setTransmission(txtTransmissionEdit.getText().trim());

        int opt = JOptionPane.showConfirmDialog(null, "Desea actualizar el carro", "Confirmar salida",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (opt == 0) {

            try {
                carController.actualizarCarro(Integer.parseInt(txtCarId.getText()), car);
                txtCarId.setText("");
                txtBrandEdit.setText("");
                txtModelEdit.setText("");
                txtAnhoEdit.setText("");
                txtColorEdit.setText("");
                txtTransmissionEdit.setText("");
                listCars();
                JOptionPane.showMessageDialog(null, "Se ha Actualizado el carro con éxito");
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "No fué posible crear el Carro");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

